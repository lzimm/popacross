package com.qorporation.qluster.common;

public class Pair<A, B> {

	private A a;
	private B b;
	
	public Pair(A a, B b) {
		this.a = a;
		this.b = b;
	}
	
	public A a() { return a; }
	public B b() { return b; }
	
	@Override
	public String toString() {
		return String.format("Pair(%s, %s)", a.toString(), b.toString());
	}
	
}
